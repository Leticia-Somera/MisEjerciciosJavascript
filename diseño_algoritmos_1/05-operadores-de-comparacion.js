/*
OPERADORES RELACIONALES O DE COMPARACIÓN:
Nos sirven para hacer compraciones y nos arroja un Boolean
Mayor que: a > b            "a" es mayor que "b"
Menor que: a < b            "a" es menor que "b"
Mayor o igual que: a >= b   "a" es mayor o igual a "b"
Menor o igual que: a <= b   "a" es menor o igual a "b"
Igual: a == b               "a" es igual a "b" y "b" es igual a "a"
Distinto: !=                "a" es diferente de "b" 
Operador estricto: ===
Operador diferente estricto: !==
*/

let numero1 = 22;
let numero2 = 15;

console.log("Número 1: " + numero1);
console.log("Número 2: " + numero2);
console.log("Número 1 es mayor que número 2: " + (numero1 > numero2));
console.log("Número 1 es menor que número 2: " + (numero1 < numero2));
console.log("Número 1 es mayor o igual que número 2: " + (numero1 >= numero2));
console.log("Número 1 es menor o igual que número 2: " + (numero1 <= numero2));
console.log("Número 1 es igual que número 2: " + (numero1 == numero2));
console.log("Número 1 es diferente a número 2: " + (numero1 != numero2));